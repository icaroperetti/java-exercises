// Cada vez que o valor for alterado apresentar o valor na tela;
package br.edu.ifcvideira.Lista6;

import java.util.Scanner;

public class Ex1Principal {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		Ex1Contador cont = new  Ex1Contador();
		int op;
		
		for(;;) {
			System.out.println("Selecione a opção\n"
					+ "1 - Para incrementar\n"
					+ "2 - Para decrementar\n"
					+ "3 - Para sair");
			op = Integer.parseInt(in.nextLine());
			
			if (op == 1) {
				cont.incrementar();
			}
			
			else if (op == 2 ) {
				cont.decrementar();
			}
			else if (op == 3) {
				System.out.println("Você saiu!");
				break;
			}
			else {
				System.out.println("Opção inexistente, por favor selecione outra opção!\n");
			}
			System.out.println("O valor atual é: "+cont.getNumero());
		}
		in.close();
	}
}
